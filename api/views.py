def simulate(request):
    try:
        orders = int(request.GET.get("orders", 0))
        workers = int(request.GET.get("workers", 0))
        speed = float(request.GET.get("speed", 1))

        if orders < 0 or workers < 0 or speed <= 0:
            return JsonResponse({"error": "Invalid input values"})

        total_capacity = workers * speed

        if total_capacity == 0:
            return JsonResponse({"error": "Workers or speed cannot be zero"})

        time_required = orders / total_capacity

        if workers < 3:
            delay_risk = "HIGH"
        elif workers < 7:
            delay_risk = "MEDIUM"
        else:
            delay_risk = "LOW"

        bottleneck = (
            "Slow processing speed" if speed < 2 else
            "Insufficient workforce" if workers < 3 else
            "No major bottleneck"
        )

        return JsonResponse({
            "input": {
                "orders": orders,
                "workers": workers,
                "speed": speed
            },
            "results": {
                "total_capacity": total_capacity,
                "time_required": round(time_required, 2),
                "delay_risk": delay_risk,
                "bottleneck": bottleneck
            },
            "status": "Simulation completed successfully"
        })

    except Exception as e:
        return JsonResponse({"error": str(e)})


def optimize(request):
    orders = int(request.GET.get("orders", 100))
    speed = float(request.GET.get("speed", 1))

    best_workers = None
    best_time = float("inf")

    for workers in range(1, 21):
        capacity = workers * speed
        time_required = orders / capacity

        if time_required < best_time:
            best_time = time_required
            best_workers = workers

    return JsonResponse({
        "best_configuration": {
            "workers": best_workers,
            "time_required": round(best_time, 2)
        },
        "status": "Optimization completed"
    })
@login_required(login_url='/accounts/login/')
def admin_panel(request):

    total_users = User.objects.count()

    total_simulations = SimulationHistory.objects.count()

    total_delays = SimulationHistory.objects.filter(
        status="Delay"
    ).count()

    avg_time = SimulationHistory.objects.aggregate(
        Avg('time_required')
    )['time_required__avg']

    top_user = SimulationHistory.objects.values(
        'user__username'
    ).annotate(
        total=Count('id')
    ).order_by('-total').first()

    return render(request, 'admin_panel.html', {
        'total_users': total_users,
        'total_simulations': total_simulations,
        'total_delays': total_delays,
        'avg_time': round(avg_time or 0, 2),
        'top_user': top_user
    })
